package com.multireq.webclient;


public class UnderscoreLinks {
	private String start;
	private String next;

	// Getter Methods

	public String getStart() {
		return start;
	}

	public String getNext() {
		return next;
	}

	// Setter Methods

	public void setStart(String start) {
		this.start = start;
	}

	public void setNext(String next) {
		this.next = next;
	}
}